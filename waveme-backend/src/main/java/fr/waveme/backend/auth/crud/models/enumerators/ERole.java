package fr.waveme.backend.auth.crud.models.enumerators;

/**
 * ERole is an enumeration that defines the different roles a user can have in the application.
 * It includes three roles: USER, MODERATOR, and ADMIN.
 * This enumeration is used to manage user permissions and access levels within the application.
 */
public enum ERole {
    ROLE_USER,
    ROLE_MODERATOR,
    ROLE_ADMIN,
}
